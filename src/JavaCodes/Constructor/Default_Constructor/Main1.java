package JavaCodes.Constructor.Default_Constructor;

public class Main1 {
    int a;
    boolean b;

    public static void main(String[] args) {
        //  A default constructor is called
        Main1 obj = new Main1();
        System.out.println("Default Value: ");
        System.out.println("a = " + obj.a);
        System.out.println("b = " + obj.b);
    }
}
